// Operadores aritméticos
let suma = 10 + 5;
let resta = 10 - 5;
let multiplicacion = 10 * 5;
let division = 10 / 5;
let modulo = 10 % 3;

// Operadores de asignación
let x = 10;
x += 5; // x ahora es 15
x -= 3; // x ahora es 12
x *= 2; // x ahora es 24
x /= 4; // x ahora es 6

// Operadores de comparación
let esIgual = (10 == "10"); // true
let esEstrictamenteIgual = (10 === "10"); // false
let esDiferente = (10 != "10"); // false
let esEstrictamenteDiferente = (10 !== "10"); // true
let mayorQue = (10 > 5); // true
let menorQue = (10 < 5); // false
let mayorOIgualQue = (10 >= 10); // true
let menorOIgualQue = (10 <= 10); // true

// Operadores lógicos
let y = true && false; // false
let o = true || false; // true
let no = !true; // false

console.log(suma, resta, multiplicacion, division, modulo, x, esIgual, esEstrictamenteIgual, esDiferente, esEstrictamenteDiferente, mayorQue, menorQue, mayorOIgualQue, menorOIgualQue, y, o, no);
